     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {
        $validator = Validator::make($request->all(), [
        'name' => 'required|max:255',
        ]);

        if ($validator->fails()) {
        return redirect('/')
            ->withInput()
            ->withErrors($validator);
        }

        $join = new Join;
        $join->name = $request->$name;
        $join->save();

        return redirect('/');

    }

